    private void setupLevelButtons() {
        JButton[] levelButtons = {L1Btn, L2Btn, L3Btn, L4Btn, L5Btn};
        
        for (int i = 0; i < levelButtons.length; i++) {
            if (progress.isLevelCompleted(i)) {
                levelButtons[i].setText("âœ“ Level " + (i+1));
                levelButtons[i].setEnabled(false);
                levelButtons[i].setToolTipText("Level sudah selesai");
            } else if (i > 0 && !progress.isLevelCompleted(i-1)) {
                levelButtons[i].setEnabled(false);
                levelButtons[i].setToolTipText("Selesaikan level " + i + " terlebih dahulu");
            }
        }
        
        if (progress.isLevelCompleted(4)) { 
            SkorBtn.setEnabled(true);
            SkorBtn.setToolTipText("Lihat skor akhir");
        } else {
            SkorBtn.setEnabled(false);
            SkorBtn.setToolTipText("Selesaikan Level 5 terlebih dahulu");
        }
    }
